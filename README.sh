# GREPFOR.sh
## USE
```bash
bash grepfor.sh <file of white-space separated terms to grep for> <file to grep>
```
### OR
Let `grepfor` prompt you for both:
```bash
            :-- GREP FOR --:

:-- Enter the to-grep list (should be a file w/ new-line separated strings):
../testlist
:-- Enter the file to grep:
../gitdork.sh

:-- Starting...
```

